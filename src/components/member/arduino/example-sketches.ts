export function getSketchById(id: string): ExampleSketch | undefined {
  return exampleSketches.find((s) => s.id === id);
}

export function getSketchesByCategory(category: ExampleSketch["category"]): ExampleSketch[] {
  return exampleSketches.filter((s) => s.category === category);
}
